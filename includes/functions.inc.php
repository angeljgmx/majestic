                        header("Location: ".$path."login_usuario.php");
                    }
                    //sino, actualizo la fecha de la sesión 
                    }
                else {
                        $_SESSION['sson_ufch'] = $ahora; 
                }
        }
        else{
            $session_validate = FALSE;
